number = 15

#Module 1
if number == 20:
    print("The number is equal to 20.")
else:
    print("The number is not equal to 20.")

#Module 2
if number != 20:
    print("The number is not equal to 20.")
else:
    print("The number is equal to 20.")

#Module 3
if number > 20:
    print("The number is greater than 20.")
else:
    print("The number is lesser than 20.")

#Module 4
if number < 20:
    print("The number is lesser than 20.")
else:
    print("The number is greater than 20.")

#Module 5
if number >= 20:
    print("The number is greater than or equal to 20.")
else:
    print("The number is lesser than or equal to 20.")

#Module 6
if number <= 20:
    print("The number is lesser than or equal to 20.")
else:
    print("The number is greater than or equal to 20.")